#What is Copy
#-->In Python, we use = operator to create a copy of an object.It only creates a new variable that shares the reference of the original object.

#What is Deep Copy
#-->A deep copy constructs a new compound object and then, recursively ,inserts copies into it of the objects found in the original

# What is Shallow Copy?
#--> A shallow copy constructs a new compound object and then (to the extent possible ) inserts refernces into it to the objects found in the original.